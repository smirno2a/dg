IntPt2d *getGQTPts(int order)
{ 
  if(order <=10)
    return GQT[order];

  int n = (order+3)/2;
  int index = n-4;
  if(!GQTdegen[index]){
    int npts = n*n;
    GQTdegen[index] = new IntPt2d[npts];
    GaussLegendreTri(n,n,GQTdegen[index]);
  }
  return GQTdegen[index]; 

}

int getNGQTPts(int order)
{ 
  if(order < 11)
    return GQTnPt[order]; 
  else
    return ((order+3)/2)*((order+3)/2);
}

